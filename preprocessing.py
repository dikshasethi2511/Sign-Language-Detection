def preprocessing_image(path):    
    image = cv2.imread(path)
    
    gray_scale = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
    blurred_image = cv2.GaussianBlur(gray_scale,(5,5),2)

    th3 = cv2.adaptiveThreshold(blurred_image,255,cv2.ADAPTIVE_THRESH_GAUSSIAN_C,cv2.THRESH_BINARY_INV,11,2)
    ret, preprocessed_image = cv2.threshold(th3, 75, 255, cv2.THRESH_BINARY_INV+cv2.THRESH_OTSU)
    return preprocessed_image

